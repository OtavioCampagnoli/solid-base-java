package conditionals;

public class EvenOrOdd {
    public static void main(String[] args) {
        int number = 24;

        if (number % 2 == 0) {
            System.out.println("The number: " + number + " is Even.");
        } else {
            System.out.println("The number: " + number + " is Odd.");
        }
    }
}
